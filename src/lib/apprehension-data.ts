function parseDate(dateString: string): string {
    const parts = dateString.split('/');
    if (parts.length === 3) {
      const [day, month, year] = parts;
      // Ensure parts are valid numbers before creating a date string.
      const yearNum = parseInt(year, 10);
      const monthNum = parseInt(month, 10);
      const dayNum = parseInt(day, 10);

      if (!isNaN(dayNum) && !isNaN(monthNum) && !isNaN(yearNum)) {
         // Pad with leading zeros if necessary
        const monthStr = String(monthNum).padStart(2, '0');
        const dayStr = String(dayNum).padStart(2, '0');
        return `${yearNum}-${monthStr}-${dayStr}`;
      }
    }
    // Return a default or throw an error if the format is incorrect.
    // Using a fixed date for invalid entries ensures consistency.
    return '2024-01-01';
}


